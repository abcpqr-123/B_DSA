// i = i+1 --> 
// i++ -> Post Increment
// ++i -> Pre Increment
// i+=1 

// i = i-1 --> 
// i-- -> Post decrement 
// --i -> Pre decrement
// i-=1



let i = 7

console.log(++i)
// 8
console.log(i++)
// 8, i = 9
console.log(i--)
// 9, i = 8
console.log(--i)
// 7, i = 7


// Output:
// 8
// 8
// 9
// 7