let a = 4
let b = 5

console.log('a & b ==> ' + (a & b))
console.log('a | b ==> ' + (a | b))
console.log('~a ==> ' + ~a)
console.log('a ^ b ==> ' + (a ^ b))


// Output:
// a & b ==> 4
// a | b ==> 5
// ~a ==> -5
// a ^ b ==> 1


